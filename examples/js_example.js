/**
# Sum function

**Overview**: Returns the sum of two numbers

Parameters:

- a: Numeric variable
- b: Numeric variable

## Example

result=sum(5, 10)

**/


//' > This function receives only two parameters

function sum(a, b) {
    return a + b;
}



/**
# Product function

**Overview**: Returns the product of two numbers

Parameters:

- a: Numeric variable
- b: Numeric variable

## Example

result=product(5, 10)
**/


function product(a, b) {
    return a * b;
}



